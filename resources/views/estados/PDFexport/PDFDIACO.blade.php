        <p style="text-align: justify;"> <strong><p>CUARTA. <u>DERECHOS DEL EDUCANDO, PADRE DE FAMILIA O REPRESENTANTE LEGAL.</u></p></strong> Como usuarios del servicio educativo contratado en armonía con el Artículo 4 de la Ley de Protección al Consumidor y Usuario, tendrá 
            derecho a:</p>
            <ul>
                <li style="text-align: justify;">A. <strong>La protección a la vida, salud y seguridad en la adquisición, consumo y uso de bienes y servicios:</strong>  Las 
                    instalaciones del centro educativo, están adecuadas para que los educandos no corran ninguna clase de 
                    riesgo que ponga en peligro su integridad física, siempre y cuando haga uso adecuado de las mismas, también 
                    están dotadas de todos los servicios básicos ofrecidos a los padres de familia. En el caso que la prestación del 
                    servicio de transporte sea brindado por una entidad ajena al centro educativo, este proporcionará todas las 
                    medidas de seguridad necesarias para la debida protección de los educandos, liberando a su vez al Centro 
                    escolar de cualquier responsabilidad de cualquier índole que se le pretendiera imputar en tal sentido; así mismo, 
                    propondrán medidas de seguridad a los padres a través de un seguro escolar, el cual no será obligatorio, pero 
                    debe quedar en acta o constancia escrita de la decisión del responsable del educando de la adquisición o no del 
                    mismo, asumiendo éste cualquier responsabilidad que se pudiera generar por la decisión de no contratar el 
                    mismo. Se debe de velar por la seguridad de los educandos, en cualquier actividad propuesta y hacerlo siempre 
                    del conocimiento del Responsable del Educando, para su respectivo consentimiento de participación.</li>
                <li style="text-align: justify;">B. <strong>La libertad de elección del bien o servicio:</strong> Los padres de familia tienen el derecho de poder adquirir, tanto los 
                    útiles escolares, como los uniformes, transporte, seguro y otros servicios adicionales, en el establecimiento 
                    comercial que se adecúe mejor a su capacidad económica; sin embargo, el centro educativo puede facilitar la 
                    compra o prestación de servicios, siempre y cuando medie convenio por escrito, en el cual lo solicitan los padres 
                    de familia, debiendo en tal caso, cumplir con las obligaciones tributarias correspondientes.
                    <p style="text-align: justify;"><u>Respecto a los seguros de gastos médicos y cualesquiera otros seguros que el padre de familia decida 
                        contratar, el Centro Educativo solicita como mínimo que estos cubran accidentes y traslados en ambulancia a 
                        centros asistenciales.</u></p>
                    <p style="text-align: justify;"><u>En caso el Responsable del Educando decida adquirir los bienes indicados en la presente cláusula a 
                        proveedores distintos del Centro Educativo, los mismos deben de ser aceptados siempre y cuando tengan las 
                        características necesarias para su utilización, con el objeto que dicha igualdad de insumos asegure una inclusión 
                        total del educando y se facilite el trabajo académico tanto del educando como de los docentes del Centro 
                        Educativo.</u></p>
                    </li>
                <li style="text-align: justify;">C. <strong>La libertad de contratación:</strong> El representante del educando, tiene el derecho a la libre contratación iniciando 
                    por la elección del Centro Educativo en el que ha decidido inscribir al Educando, aceptando que dicho Centro 
                    Educativo es la mejor opción que se adecúa a sus requerimientos educativos y capacidades económicas. En 
                    igual sentido, puede contratar o adquirir los bienes o servicios (Transporte, Seguro Escolar y otros), que más se 
                    adecúen a su capacidad económica, siempre y cuando cumplan en su totalidad con las autorizaciones y 
                    requerimientos operativos, de seguridad y de cualquier otra índole requeridos por la legislación vigente, 
                    debiendo para el efecto informar al Centro Educativo de tal contratación, previo al inicio del ciclo escolar.</li>
                <li style="text-align: justify;">D. <strong>a información veraz, suficiente, clara y oportuna sobre los bienes y servicios:</strong>  El centro educativo se 
                    compromete a proporcionar al Responsable del Educando, la información completa sobre el servicio contratado 
                    y especialmente los horarios de clases, los grados y las carreras autorizadas que se imparten, los sistemas de 
                    evaluación, reglamentos, manuales y políticas, que se fundamente con las disposiciones legales y 
                    reglamentarias vigentes, cursos adicionales que imparten, el monto de las cuotas que cobran, tanto de 
                    inscripción como de cuota mensual; así como, de las actividades extraescolares de carácter voluntario u 
                    optativas, que se puedan realizar durante el ciclo escolar respectivo. Cuando el responsable del educando esté 
                    de acuerdo en contratar los servicios antes indicados manifiesta de manera expresa, espontánea y voluntaria 
                    cumplir en su totalidad y comprometerse a que el Educando también los cumpla, aceptando a su vez las 
                    consecuencias que genere cualquier incumplimiento a los mismos, siempre y cuando dichas consecuencias no 
                    menoscaben el derecho a la educación que le asiste al educando. Las autoridades del centro educativo, tienen 
                    la obligación de cumplir con las leyes y Acuerdos Ministeriales aplicables a su actividad.</li>
                <li style="text-align: justify;">E. <strong>Utilizar el Libro de Quejas o el medio legalmente autorizado por la Dirección de Atención y Asistencia al 
                    Consumidor, para dejar registro de su disconformidad con respecto a un bien adquirido o servicio 
                    contratado:</strong>  Al hacer constar su inconformidad en el libro de quejas el firmante debe esperar un período de ocho 
                    días para que la misma sea resuelta por las autoridades del centro educativo, transcurrido ese tiempo sin que 
                    exista una solución satisfactoria deberá interponer la queja correspondiente ante la DIACO quien procederá 
                    según corresponda</p></li>
            </ul>
