    <p>La finalidad de este reglamento es promover y desarrollar en toda la comunidad educativa los principios y
        elementos que construyan una sana convivencia, formación, y funcionamiento de la institución que redunde en
        beneficio de los alumnos </p>
    <strong><p>1. HORARIOS, ASISTENCIA Y PRESENTACIÓN :</p></strong>
        <ul>
        <li>
            1.1 El horario de clases es de 7:25 a 12:10 para pre- primaria, de 7:15 a 13:15 para primaria y básicos. 
        </li>
        <li>
            1.2 La puerta se cierra a las 7:25 horas en punto y no se permite el ingreso a ningún alumno a clases hasta que
            se le entregue reporte. Después de 3 reportes consecutivos no podrá ingresar        
        </li>
        <li>
            1.3 Los alumnos deben asistir puntualmente desde el primer día de clases y, si faltan por alguna causa deben
            traer una justificación por escrito en la agenda.
        </li>
        <li>
            1.4 El alumno debe acumular un 80% de la asistencia para tener derecho a evaluaciones
        </li>
        <li>
            1.5 Se permite solo en casos excepcionales que los padres o encargados recojan a los niños antes de la hora de
            salida y no se entregará a ningún alumno a otra persona que no esté autorizada por ellos por medio de una
            nota con firma que se pueda corroborar en la ficha de inscripción
        </li>
        <li>
            1.6 Los alumnos que durante el ciclo escolar, se retiren solos a sus casas deben tener una nota especificándolo
            en agenda, de no tenerla, no se le permitirá el egreso si no es con un adulto responsable
        </li>
        <li>
            1.7 Si el alumno va a irse con un compañero debe traer la nota de autorización de sus padres en la agenda, ya
            que no se permite realizar llamadas del colegio a última hora. 
        </li>
        <li>
            1.8 El uniforme incluye: jumper/ falda o pantalón de la tela especifica, blusa/ camisa tipo polo con diseño en el
           cuello, sweater con monograma y debidamente identificado con el nombre bordado en la parte izquierda
           superior del mismo, zapatos negros, Pants y chumpa respectiva, camiseta con el logo del colegio y tenis
           obscuros. 
        </li>
        <li>
            1.9 No se permite que los varones tengan el pelo largo, colas o cortes de pelo extravagantes ni tipo hongo. Las
            niñas no deben venir con uñas pintadas, maquillaje ni faldas excesivamente cortas. Tampoco se permite el
            uso de collares, pulseras o pitas (típicas, de plástico o cuero), pircing o tatuajes. 
        </li>
        <li>
            1.10 Es obligatorio asistir diariamente con el uniforme completo (diario o física) y a partir de febrero se le
            enviara reporte.
        </li>
        <li>
            1.11 Si a pesar de lo indicado anteriormente algún alumno se presenta sin uniforme, se le enviara reporte, las
            excusas y notas de los padres de familia no exime la entrega del reporte.
        </li>
        <li>
            1.12 Los alumnos pueden celebrar en el colegio su cumpleaños con piñata y/o refacción, solicitando en
            secretaría la autorización de la fecha. No se pueden hacer celebraciones en días de actividades especiales.
            Este único día el alumno cumpleañero puede venir de particular.
        </li>
        <li>
            1.13 Los padres de familia deben respetar la privacidad de las personas que laboran en esta institución por lo
            que no deben tratar nada relacionado con el colegio por teléfono o personalmente en sus casas. Tampoco se
            permite que los aborden a la hora de entrada o salida en las calles aledañas al colegio.
        </li>
       </ul> 


    <strong><p>2. UTILES Y MATERIALES.</p></strong>
    <ul>
        <li>
            2.1 Todos los útiles escolares y libros de texto deben estar completos y forrados antes que se inicien las clases.
(No es obligatorio adquirir los libros en el colegio). 
        </li>
        <li>
            2.2 Los primeros días de enero se entregan en el aula respectiva todos los cuadernos, libros y materiales que se
piden en la lista de útiles. De preparatoria en adelante no se entregan lapiceros, lápices, crayones,
borradores y sacapuntas.
        </li>
        <li>
            2.3 Todas las prendas u objetos personales deben estar debidamente identificados con el nombre completo del
alumno.
        </li>
        <li>
            2.4 El colegio no se hace responsable por objetos que no se hayan pedido por escrito como aparatos
electrónicos, juguetes, Cd, ipods, Tablet, computadoras portátiles y otros que traigan. Está prohibido el uso
de celulares dentro del plantel.
        </li>
        <li>
            2.5 No está permitido que los maestros pidan a los niños contribuciones, multas o recolectas para ninguna
actividad por lo que si se presenta esta situación debe notificarse inmediatamente a dirección.
        </li>
        <li>
            2.6 Cualquier material que se pida dentro del término del año será de forma escrita, por medio de una circular
con el visto bueno de la dirección. El padre de familia no está obligado a mandar absolutamente nada que
no sea pedido de esta forma, esto de ninguna manera redundará en las calificaciones.
        </li>
        <li>
            2.7 Los alumnos están obligados a reparar los daños y perjuicios que ocasionen en el colegio o a otros
compañeros, tanto a nivel individual como de grupo.
        </li>
        <li>
            2.8 Los padres de familia deben velar porque sus hijos no lleven a su casa objetos que no les pertenezcan ya
que ellos deben aprender a respetar lo ajeno y por ser esto una falta grave, el colegio la sancionará
drásticamente. 
        </li>
    </ul>

     <strong><p>3. DISCIPLINA.</p></strong>
     <ul>
        <li>
            3.1 La conducta de los alumnos, tanto dentro, como fuera del plantel, debe ser adecuada y la misma será
tomada en cuenta cada año para la reinscripción, ya que ellos representan a la institución. Si el alumno
participa fuera del colegio en cualquier acción contra la moral y buenas costumbres, en la cual se
comprometa la reputación y honor de él y por consiguiente del Colegio, el Plantel se reserva el derecho de
analizarlas y según el caso, sancionarlas, aunque no se hace responsable de las mismas.
        </li>
        <li>
            3.2 El colegio se reserva el derecho de aceptar para el siguiente año a los alumnos que estén inscritos y su
conducta no sea adecuada, al igual que aquellos alumnos cuyos padres no cumplan con las normas
establecidas, cualquier situación de inconformidad debe canalizarse por medio de dirección,
preferiblemente por escrito
        </li>
        <li>
            3.3 Cualquier comentario o acción errónea del personal docente, administrativo o de servicio debe ser
informado directamente a dirección para que se tomen las medidas respectivas.
        </li>
        <li>
            3.4 La autoridad de convocatoria es exclusiva de dirección, por lo que si cualquier padre de familia o persona
ajena a la institución promueve reuniones para tratar asuntos del colegio, la dirección solicitará el retiro
definitivo de la familia. 
        </li>
        <li>
            3.5 La disciplina se logra a base de trabajo escolar debidamente motivado, pero si en algún caso se faltare a la
misma, se llamará a los padres de familia para que conjuntamente con ellos se corrija la falta. Se envían 2
reportes como llamada de atención, luego 2 Tarjetas Amarillas por escrito, si la falta persiste y por último
hay suspensión según sea la gravedad del caso (tarjera roja).
        </li>
        <li>
            3.6 Los alumnos que por cualquier motivo se agredan físicamente dentro o fuera del colegio serán
suspendidos. El uso de palabras soeces y señales que se tomen como groserías son motivo automático de
tarjera amarilla.
        </li>
     </ul>

     <strong><p>4. RENDIMIENTO ESCOLAR Y EVALACIONES.</p></strong>
     <ul>
        <li>
            4.1 Dentro del ciclo escolar se hace un promedio de las 4 evaluaciones de cada unidad que constituyen la nota
de promoción. Según el cronograma se mandan anticipadamente guías de estudio de cada materia, que será
el contenido de las pruebas escritas, estás son calificadas sobre 60 puntos; los 40 puntos restantes se
acumulan con trabajos, tareas y orden de sus cuadernos durante toda la unidad. Los contenidos que ya
fueron evaluados no volverán a evaluarse en la siguiente unidad, excepto cuando la nota de promoción sea
menor de 60 y deban hacer prueba de recuperación
        </li>
        <li>
            4.2 Según artículo 24 del reglamento de evaluación: Todos los estudiantes serán promovidos al grado
inmediato superior, cuando obtengan un mínimo de sesenta (60) puntos de promedio en cada una de las
áreas, sub-áreas, asignaturas o su equivalente, en el plan de estudios respectivo. 
        </li>
        <li>
            4.3 Las calificaciones se entregan una semana después de las evaluaciones. Es responsabilidad del padre de
familia velar por que su hijo cumpla con los requerimientos establecidos para ser promovido.
        </li>
        <li>
            4.4 Es necesaria la comunicación constante entre los padres y maestros por lo que pueden venir al colegio a
hablar con el personal docente o las directoras los días miércoles entre 8:00 a 12:00 horas con un tiempo
máximo de 5 minutos por maestro. Para esto deben pasar a la dirección para no interrumpir las clases y se
pueda mandar una maestra sustituta al grado
        </li>
        <li>
            4.5 Los días que haya actividad especial como: exámenes o celebraciones no se atenderá a padres de familia en
asuntos relacionados con rendimiento, y/o conducta o pagos.
        </li>
        <li>
            4.6 Todos los grados desde Kinder, llevan deberes todos los días. El alumno que no cumpla con su tarea, deberá
hacerla a la hora de receso. Después de tres tareas sin entregar, amerita tarjeta amarrilla. 
        </li>
        <li>
            4.7 Con más de tres asignaturas reprobadas deben repetir el grado, solamente hay una oportunidad de
recuperación.
        </li>
     </ul>

     <strong><p>5. CUOTAS</p></strong>
<ul>
    <li>
        5.1 Las cuotas de colegiaturas deben ser canceladas por anticipado en los primeros 10 días del mes
correspondiente, estos pagos deben hacerse en el colegio en efectivo o presentando la boleta de deposito
del Banco Agromercantil cta. No. 49-0112346-9 o Banrural cta. No. 3445387086 y el talonario de pagos
debidamente lleno. A la boleta de depósito debe escribirle inmediatamente el nombre completo del alumno
y grado en el reverso de la misma. 
    </li>
    <li>
        5.2 No se recibirá ningún pago sin el talonario del colegio completo pues este es su único comprobante de
pago. La reposición del mismo tiene un costo de Q.25.00
    </li>
    <li>
        5.2 No se recibirá ningún pago sin el talonario del colegio completo pues este es su único comprobante de
pago. La reposición del mismo tiene un costo de Q.25.00
    </li>
    <li>
        5.3 Si cancela después del 10 del mes en curso son Q10.00 de mora por alumno y si cancela el siguiente mes, en
adelante, la mora es de Q20.00 por alumno. 
    </li>
    <li>
        5.4 Después de transcurrido el mes subsiguiente sin cancelar, no se permitirá el ingreso de los alumnos al
        establecimiento hasta estar solventes en sus pagos.
    </li>
    <li>
        5.5 Los depósitos realizados en los bancos, deben ser enviados a más tardar tres días después de realizado
dicho depósito de lo contrario se cobrara mora. (Atrás del voucher escribir los apellidos de los alumnos y
los grados) 
    </li>
    <li>
        5.6 Una vez hecha la inscripción, esta se considera definitiva y por lo tanto adquiere todos los derechos que da
la misma; los pagos que se hayan realizado no se devolverán ningún caso.
    </li>
    <li>
        5.7 La inasistencia a clases no exime el pago de la colegiatura correspondiente, esta obligación solo cesa al
notificar a dirección el retiro definitivo del alumno.
    </li>
</ul>

